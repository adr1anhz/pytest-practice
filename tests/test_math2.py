# Multiplication test ideas:
# two positive integers
# identity: multiply by 1
# zero: multiply by 0
# positive by negative
# negative by negative
# floats


def test_multiply_two_positive_inits():
    assert 2 * 3 == 6
    

def test_multiply_identity():
    assert 1 * 99 == 99


def test_multiply_zero():
    assert 0 * 100 == 0